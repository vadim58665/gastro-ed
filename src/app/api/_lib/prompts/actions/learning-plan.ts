export function getLearningPlanPrompt(): string {
  return `## Инструкции: учебный план

Составь структурированный учебный план по запрошенной теме.

Каждый шаг должен содержать:
- Название подтемы
- Что изучить (краткое описание)
- Примерное время (в минутах)
- Контрольные точки (как проверить усвоение)

Привязывай план к реальным темам и карточкам приложения, где возможно.

После представления плана ОБЯЗАТЕЛЬНО спроси:
"Устраивает такой формат? Могу сделать ускоренный (основные моменты за 2-3 дня) или углублённый (подробное изучение за 1-2 недели)."

Ответ в JSON:
{
  "title": "Учебный план: [тема]",
  "topic": "...",
  "intensity": "standard",
  "totalDays": число,
  "steps": [
    {
      "day": 1,
      "title": "...",
      "description": "...",
      "estimatedMinutes": число,
      "checkpoints": ["..."]
    }
  ],
  "summary": "краткое описание плана"
}`;
}
