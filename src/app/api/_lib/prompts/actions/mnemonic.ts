export function getMnemonicPrompt(type: "acronym" | "story" | "rhyme" | "poem"): string {
  const base = `Создай мнемонику для запоминания медицинского материала.
Требования:
- Клиническая точность обязательна
- Все факты должны соответствовать актуальным КР
- Ответ строго в JSON`;

  switch (type) {
    case "acronym":
      return `${base}

Формат: мнемонический АКРОНИМ на русском.
Каждая буква = один конкретный клинический концепт.
Буквы должны образовывать запоминающееся слово.

Ответ в JSON: { "mnemonic": "СЛОВО - расшифровка каждой буквы", "explanation": "что каждая буква означает", "facts_covered": ["факт1", "факт2"] }`;

    case "story":
      return `${base}

Формат: короткая яркая ИСТОРИЯ (3-5 предложений).
Медицинские факты вплетены в запоминающийся сюжет.
Используй визуальные образы и сенсорные детали.

Ответ в JSON: { "story": "текст истории", "facts_covered": ["факт1", "факт2"] }`;

    case "rhyme":
      return `${base}

Формат: рифмованная мнемоника (4-6 строк).
Требования: чёткая рифма, ритм, медицинская точность.
Естественный русский язык, не натужные рифмы.

Ответ в JSON: { "rhyme": "текст", "facts_covered": ["факт1", "факт2"] }`;

    case "poem":
      return `${base}

Формат: стихотворение для запоминания (6-10 строк).
Требования: ритм, рифма, все ключевые факты включены.
Должно легко читаться вслух и запоминаться.

Ответ в JSON: { "poem": "текст стихотворения", "facts_covered": ["факт1", "факт2"] }`;
  }
}
