def model_for(content_type: ContentType) -> str:
    return HAIKU if content_type == ContentType.HINT else SONNET


def max_tokens_for(content_type: ContentType) -> int:
    if content_type == ContentType.HINT:
        return 200
    if content_type == ContentType.EXPLAIN_SHORT:
        return 400
    return 1500


def estimate_cost_usd(model: str, input_tokens: int, output_tokens: int) -> float:
    if model not in MODEL_COSTS_PER_M:
        return 0.0
    input_cost, output_cost = MODEL_COSTS_PER_M[model]
    return (input_tokens * input_cost + output_tokens * output_cost) / 1_000_000.0
